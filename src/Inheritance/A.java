package Inheritance;

class A{
	public void print_A() {
		System.out.println("class A");
	}
	}
class B extends A{
	public void print_B() {
		System.out.println("class B");
	}
	
}
class C extends A{
	public void print_C() {
		System.out.println("class C");
	}
	
}
class D extends A{
	public void print_D() {
		System.out.println("class D");
	}
	
}




